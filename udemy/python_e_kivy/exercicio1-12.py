#Faça um algoritmo que solicite a largura e a altura de um retângulo.
# Em seguida, imprima para o usuário quantos metros quadrados possui a área total.

largura = float(input("Digite o valor da largura do retangulo: "))
altura = float(input("Digite o valor da altura do retangulo: "))
print()
print("O retângulo de largura %.2f e de altura %.2f possui a área de %.2f" %(largura, altura, largura * altura))